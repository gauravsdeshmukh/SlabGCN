def VegardsLaw(comp_dict, c=0.0171):
    """Vegard's law function for the PtPdRhCuAu HEA.

    Parameters
    ----------
    comp_dict: dict
        Dictionary with "Pd", "Pt", "Rh", "Cu", and "Au" as keys and their
        respective compositions (adding to 1) as values.
    c: float (default = 0.0171)
        Correction to standard Vegard's law expression.

    Returns
    -------
    a: float
        Calculated lattice constant.

    """
    # Pure metal lattice constants
    a_dict = {}
    a_dict["Pt"] = 3.97
    a_dict["Pd"] = 3.95
    a_dict["Cu"] = 3.63
    a_dict["Au"] = 4.17
    a_dict["Rh"] = 3.84

    # Calculate lattice constant
    a = c
    for element in a_dict.keys():
        a += comp_dict.get(element, 0) * a_dict[element]

    return a

